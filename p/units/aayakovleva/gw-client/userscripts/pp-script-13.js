// Конфигурация цветовой схемы страницы оплаты — единая точка управления.
// Меняешь значение здесь → перекрашивается вся страница: и кнопки правой колонки
// (ОПЛАТИТЬ ЗАКАЗ, «Применить» промокода, частичная оплата), и кнопки рассрочек/
// кредитов/безнала (контурные коралловые), и рамки/иконки/ссылки — потому что в CSS
// (payment-page.css) все эти места ссылаются на CSS-переменные через var(--…).
// Скрипт переопределяет переменные :root инлайном на <html>, поэтому JS-константы ниже
// имеют приоритет над дефолтами в CSS (:root). Если скрипт не отработает — останутся
// дефолты из CSS, страница не «слетит». Порядок запуска среди скриптов не важен.
// xdgetId: r9013
;(function () {
  // === ЕДИНЫЙ ИСТОЧНИК ИСТИНЫ ЦВЕТОВОЙ СХЕМЫ ===
  // Достаточно поменять --main-color, чтобы сменить акцент всей страницы.
  var PALETTE = {
    '--main-color':
      (window.__PP_CONFIG__ &&
        window.__PP_CONFIG__.general &&
        window.__PP_CONFIG__.general.accentColor) ||
      '#F85C50', // акцент: кнопки, рамки, ссылки, иконки
    '--card-bg': '#f5f5f5', // нейтральный фон/рамка карточки метода оплаты
    '--panel-bg': '#fafafa', // мягкая плашка: состав заказа, плательщик, частичная
    '--panel-border': '#ececec', // рамка мягкой плашки
    '--btn-disabled': '#d3d3d3', // фон заблокированной кнопки оплаты
    '--btn-secondary': '#2b2b2b' // вторичная кнопка в карточке метода (графит в покое, коралл на hover)
  }

  var root = document.documentElement
  Object.keys(PALETTE).forEach(function (name) {
    root.style.setProperty(name, PALETTE[name])
  })
})()
